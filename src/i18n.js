function detectLocale() {
  if (typeof localStorage === 'undefined') return 'ja'; // Node (テスト) 環境
  const saved = localStorage.getItem('locale');
  if (saved && LOCALES[saved]) return saved;
  const nav = (navigator.language || 'ja').toLowerCase();
  if (nav.startsWith('ja')) return 'ja';
  if (nav.startsWith('zh')) return 'zh';
  if (nav.startsWith('ko')) return 'ko';
  return 'en';
}

export let locale = detectLocale();

/** 現在の言語で翻訳を取得する。{var} を vars で展開。無ければ日本語にフォールバック */
export function t(key, vars = {}) {
  let s = LOCALES[locale][key] ?? LOCALES.ja[key] ?? key;
  for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
  return s;
}

/** 言語を切り替えて画面に即時反映する (リロードなし) */
export function setLocale(code) {
  if (!LOCALES[code]) return;
  locale = code;
  localStorage.setItem('locale', code);
  applyStaticI18n();
}

export const localeNames = Object.fromEntries(
  Object.entries(LOCALES).map(([code, dict]) => [code, dict.name])
);

/** data-i18n / data-i18n-title / data-i18n-placeholder 属性を持つ要素を一括翻訳 */
export function applyStaticI18n() {
  document.documentElement.lang = locale;
  for (const el of document.querySelectorAll('[data-i18n]')) {
    el.textContent = t(el.dataset.i18n);
  }
  for (const el of document.querySelectorAll('[data-i18n-title]')) {
    el.title = t(el.dataset.i18nTitle);
  }
  for (const el of document.querySelectorAll('[data-i18n-placeholder]')) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  }
}
